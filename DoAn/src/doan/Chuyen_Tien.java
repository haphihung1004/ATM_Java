    private void btn_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_OKActionPerformed

        if (txt_Tai_Khoan_Nhan.getText() != null && !(txt_Tai_Khoan_Nhan.getText()).trim().isEmpty()) {
            if ((txt_Tai_Khoan_Nhan.getText()).compareTo(tenTK) != 0) {
                if (KiemTraTaiKhoan(txt_Tai_Khoan_Nhan.getText()) == 0) {
                    JOptionPane.showMessageDialog(this, "Tài khoản nhận tiền không tồn tại !!!!");
                    txt_Tai_Khoan_Nhan.requestFocus();
                } else {
                    if (txt_So_Tien_Chuyen.getText() != null && !(txt_So_Tien_Chuyen.getText()).trim().isEmpty()) {
                        try {
                            if (Float.parseFloat(txt_So_Tien_Chuyen.getText()) < SoTienTrongTaiKhoan()) {
                                if (Float.parseFloat(txt_So_Tien_Chuyen.getText()) > 50000 && Float.parseFloat(txt_So_Tien_Chuyen.getText()) % 2 == 0) {
                                    ConnectionDB con = new ConnectionDB();
                                    String sql = "update UserN set SoTienTaiKhoan='" + (SoTienTrongTaiKhoan() - Float.parseFloat(txt_So_Tien_Chuyen.getText())) + "'  where username='" + l.user_dang_nhap + "'";
                                    int dialogButtons = JOptionPane.YES_NO_CANCEL_OPTION;
                                    int dl = JOptionPane.showConfirmDialog(this, "Bạn có muốn chuyển tiền không?", "Thông báo", dialogButtons);
                                    if (dl == 0) {
                                        con.s.executeUpdate(sql);
                                        Date date = java.util.Calendar.getInstance().getTime();
                                        con.s.executeUpdate("Insert Chuyentien values('" + date + "','" + tenTK + "','" + txt_Tai_Khoan_Nhan.getText() + "','" + Float.parseFloat(txt_So_Tien_Chuyen.getText()) + "')");
                                        con.s.executeUpdate("Insert Nhantien values('" + date + "','" + txt_Tai_Khoan_Nhan.getText() + "','" + tenTK + "','" + Float.parseFloat(txt_So_Tien_Chuyen.getText()) + "')");
                                        JOptionPane.showMessageDialog(this, "Bạn đã chuyển tiền thành công!!");
                                        txt_Tai_Khoan_Nhan.setText("");
                                        txt_So_Tien_Chuyen.setText("");
                                        txt_Tai_Khoan_Nhan.requestFocus();
                                    }

                                } else {
                                    JOptionPane.showMessageDialog(this, "Số tiền cần chuyển phải lớn hơn 50000 và là bội của 50000!!");
                                    txt_So_Tien_Chuyen.requestFocus();
                                }
                            } else {
                                JOptionPane.showMessageDialog(this, "Số tiền trong tài khoản không đủ để thực hiện giao dịch!!");
                                txt_So_Tien_Chuyen.requestFocus();
                            }

                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(this, "Loi " + e);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Bạn phải nhập số tiền mới thực hiện thao tác này!!");
                        txt_Tai_Khoan_Nhan.requestFocus();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tài khoản nhận tiền không thể trùng với tài khoản của bạn !!");
                txt_Tai_Khoan_Nhan.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập tài khoản nhận tiền trước!!");
            txt_Tai_Khoan_Nhan.requestFocus();
        }
    }//GEN-LAST:event_btn_OKActionPerformed

