    public float SoTienTrongTaiKhoan() {
        float fTien = 0;
        try {
            ConnectionDB con = new ConnectionDB();
            String sql = "select * from UserN where username='" + tenTK + "'";

            ResultSet rs = con.s.executeQuery(sql);
            while (rs.next()) {
                fTien = Float.parseFloat(rs.getString("SoTienTaiKhoan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi " + e);
        }
        return fTien;
    }

    public int KiemTraTaiKhoan(String userNguoiNhan) {
        int check = 0;
        try {
            ConnectionDB con = new ConnectionDB();
            String sql = "SELECT * FROM DangNhap WHERE username ='" + userNguoiNhan + "'";

            ResultSet rs = con.s.executeQuery(sql);
            if (rs.next()) {
                check = 1;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi " + e);
        }
        return check;
    }

